// The following line includes the standard input/output library,
// allowing the program to use functions like printf for output.
#include <stdio.h>

// The main function is the entry point of every C program.
// It returns an integer value (int).
int main() {

    // The printf function is used to print text to the console.
    // In this case, it prints the string "Hello, World!".
    printf("Hello, World!");

    // The return statement indicates the end of the main function
    // and returns the value 0 to the operating system, signifying
    // successful execution. Conventionally, a return value of 0
    // indicates success.
    return 0;
}
