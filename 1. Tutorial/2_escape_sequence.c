#include <stdio.h>

int main() {
    // The \n escape sequence represents a new line.
    printf("This is an example of a new line: \n");

    // The \t escape sequence represents a horizontal tab.
    printf("This is an example of a tab: \t TAB\n");

    // The \\ escape sequence represents a backslash.
    printf("This is an example of a backslash: \\ \n");

    // The \" escape sequence represents a double quote.
    printf("This is an example of a double quote: \" \n");

    // The %% escape sequence represents a percent sign.
    printf("This is an example of a percent sign: %% \n");

    return 0;
}
